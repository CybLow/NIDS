set<string> getUniqueServices() {
    set<string> uniqueServices;
    for (const auto& pair : portToServiceMap) {
        uniqueServices.insert(pair.second);
    }
    return uniqueServices;
}

string getServiceNameByPort(int port) {
    auto it = portToServiceMap.find(port);
    if (it != portToServiceMap.end()) {
        return it->second;
    } else {
        return "Unknown";
    }
}


